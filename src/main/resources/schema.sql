    small_variant_id BIGINT NOT NULL,
    gene VARCHAR(256),
    gene_id VARCHAR(256),
    transcript VARCHAR(512),
    consequence VARCHAR(1024),
    impact VARCHAR(128),
    hgvs_c VARCHAR(1024),
    hgvs_p VARCHAR(1024),
    clinvar_status VARCHAR(1024),
    population_af DOUBLE PRECISION,
    annotation_source VARCHAR(256),
