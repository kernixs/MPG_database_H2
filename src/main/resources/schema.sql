CREATE INDEX IF NOT EXISTS idx_small_annotations_consequence
    ON small_variant_annotations(consequence);

CREATE TABLE IF NOT EXISTS small_variant_attributes (
    attribute_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    small_variant_id BIGINT NOT NULL,
    annotation_name VARCHAR(256) NOT NULL,
    text_value VARCHAR(4096),
    numeric_value DOUBLE PRECISION,
    boolean_value BOOLEAN,
    value_type VARCHAR(32) NOT NULL,
    source_name VARCHAR(256),
    source_version VARCHAR(256),
    raw_value VARCHAR(8192),
